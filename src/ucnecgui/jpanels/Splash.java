    /**
     *Carga una fuente personalizada para poder ser utilizada en los componentes de texto del panel
     */
    public void FontLoader() {
        try {
            //create the font to use. Specify the size!
            Font customFont = Font.createFont(Font.TRUETYPE_FONT, new File("Fonts\\OpenSans-Regular.ttf")).deriveFont(40f);
            Font smallCustomFont = Font.createFont(Font.TRUETYPE_FONT, new File("Fonts\\OpenSans-Regular.ttf")).deriveFont(14f);
            GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
            //register the font
            ge.registerFont(Font.createFont(Font.TRUETYPE_FONT, new File("Fonts\\OpenSans-Regular.ttf")));
            this.jLabel1.setFont(customFont);            
            this.jLabel3.setFont(customFont);
            this.jLabel1.setForeground(new Color(36,113,163));
            this.jLabel4.setFont(smallCustomFont);
        } catch (IOException | FontFormatException e) {
        }

    }

