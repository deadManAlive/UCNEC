package ucnecgui.models;

/**
 *
 * @author Leoncio Gómez
 */

public class PolarData {

    private double r;
    private double angle;

    /**
     *Constructor de la clase PolarData a partir de un valor de radio r y un ángulo polar angle
     * @param r Radio polar
     * @param angle Ángulo polar (grados)
     */
    public PolarData(double r, double angle) {
        this.r = r;
        this.angle = angle;
    }

    /**
     * @return the r
     */
    public double getR() {
        return r;
    }

    /**
     * @param r the r to set
     */
    public void setR(double r) {
        this.r = r;
    }

    /**
     * @return the angle
     */
    public double getAngle() {
        return angle;
    }

    /**
     * @param angle the angle to set
     */
    public void setAngle(double angle) {
        this.angle = angle;
    }
}
